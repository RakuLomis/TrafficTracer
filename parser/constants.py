# Phase enum from net/log/net_log.h
PHASE_NAMES = {
    0: "PHASE_BEGIN",
    1: "PHASE_END",
    2: "PHASE_NONE",
}


class NetLogConstants:
    """Parsed constants from a NetLog dump."""

    def __init__(self, raw_constants: dict[str, Any]):
        self.raw = raw_constants
        self.log_format_version: int = raw_constants.get("logFormatVersion", 1)

        # timeTickOffset converts "time ticks" to Unix epoch milliseconds.
        self.time_tick_offset: float = float(raw_constants.get("timeTickOffset", 0))

        # Merge hardcoded type names with whatever the log provides.
        # The log file's constants are authoritative for numeric→name mapping;
        # our hardcoded table fills gaps for visualization.
        self.event_type_names: dict[int, str] = dict(EVENT_TYPE_NAMES)
        self.source_type_names: dict[int, str] = dict(SOURCE_TYPE_NAMES)
        self.event_type_names.update(
            _invert_map(raw_constants.get("logEventTypes", {}))
        )
        self.source_type_names.update(
            _invert_map(raw_constants.get("logSourceType", {}))
        )

        # Error codes
        self.net_error: dict[int, str] = _invert_map(
            raw_constants.get("netError", {})
        )
        self.quic_error: dict[int, str] = _invert_map(
            raw_constants.get("quicError", {})
        )

        self.client_info: dict[str, Any] = raw_constants.get("clientInfo", {})

    def ticks_to_unix_ms(self, ticks: str | int | float) -> float:
        """Convert time ticks to Unix epoch milliseconds."""
        return self.time_tick_offset + float(ticks)

    def get_event_name(self, type_id: int) -> str:
        return self.event_type_names.get(type_id, f"UNKNOWN({type_id})")

    def get_source_name(self, type_id: int) -> str:
        return self.source_type_names.get(type_id, f"UNKNOWN({type_id})")

    def get_phase_name(self, phase_id: int) -> str:
        return PHASE_NAMES.get(phase_id, f"UNKNOWN({phase_id})")

    def get_net_error_name(self, code: int) -> str:
        return self.net_error.get(code, str(code))


def _invert_map(d: dict[str, int]) -> dict[int, str]:
    """Invert {name: id} → {id: name}."""
    return {v: k for k, v in d.items()}
